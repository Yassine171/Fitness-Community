package com.example.mobile_pfe.model;

public class Coach {

    String name;
    String rating;
    String description;
    int image ;

    public Coach(String name,String rating,String description, int image) {
        this.name = name;
        this.rating = rating;
        this.description = description;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
